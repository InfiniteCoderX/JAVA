package JAVA.Loops;

public class ForLoop {
    
    public static void main(String[] args) {
        ForLoop.OneToTen(5);
    }
     
    
    static void OneToTen(int n){
        
        // 1 to 10
        
        for(int i = 1; i <= n; i++){
            System.out.println(i);
        }
    }


}
